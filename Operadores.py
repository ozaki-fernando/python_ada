#Operações matemáticas (Aritméticas)

numero1 = 10
numero2 = 20

print (numero1 + numero2) #soma
print (numero1 - numero2) #subtração
print (numero1 * numero2) #multiplicação
print (numero1 / numero2) #Divisão
print (numero1 // numero2) #Divisão inteira (descarta o numero após a virgula)
print (20 // 3)
print (20 % 3) #resto da divisão
print (2 ** 3) #exponenciação (primeiro numero elevado pelo segundo)

